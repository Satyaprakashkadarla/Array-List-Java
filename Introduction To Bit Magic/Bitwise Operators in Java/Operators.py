# Bitwise AND (&) Example
operand1 = 0b1010  # Binary representation of 10
operand2 = 0b1100  # Binary representation of 12
result_and = operand1 & operand2  # Bitwise AND
print("Bitwise AND:", bin(result_and))  # Output: 0b1000

# Bitwise OR (|) Example
result_or = operand1 | operand2  # Bitwise OR
print("Bitwise OR:", bin(result_or))  # Output: 0b1110

# Bitwise XOR (^) Example
result_xor = operand1 ^ operand2  # Bitwise XOR
print("Bitwise XOR:", bin(result_xor))  # Output: 0b110

# Bitwise NOT (~) Example
operand3 = 0b00001010  # Binary representation of 10
result_not = ~operand3   # Bitwise NOT
print("Bitwise NOT:", bin(result_not))  # Output: -0b1011

# Left Shift (<<) Example
operand4 = 0b00001010  # Binary representation of 10
result_left_shift = operand4 << 2   # Left shift by 2 positions
print("Left Shift:", bin(result_left_shift))  # Output: 0b101000

